export const getExercisesByCategory = (cat: string) => exercises.filter(e => e.category === cat);
export const getExercisesByTrack = (track: string) => exercises.filter(e => e.tracks.includes(track as any));
export const getExercisesByDifficulty = (diff: string) => exercises.filter(e => e.difficulty === diff);
